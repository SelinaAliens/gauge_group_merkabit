# =========================================================================
# BFS distance from element e to target set, using RIGHT multiplication
# by all group elements. Distance = min word length to reach target.
# =========================================================================

def bfs_distance_to_set(start, target_set, mul_table):
    """
    BFS distance from start element to nearest element of target_set.
    Edges: right-multiply by any group element (= Cayley graph distance).

    But full Cayley graph has diameter 1 (every element is g*h for some h).
    So we use GENERATORS instead — minimal generating set of PSL(2,7).
    """
    from collections import deque
    if start in target_set:
        return 0
    visited = {start}
    queue = deque([(start, 0)])
    while queue:
        current, dist = queue.popleft()
        for gen in gen_set:
            # Right multiply
            nxt = mul_table[current, gen]
            if nxt in target_set:
                return dist + 1
            if nxt not in visited:
                visited.add(nxt)
                queue.append((nxt, dist + 1))
            # Left multiply
            nxt2 = mul_table[gen, current]
            if nxt2 in target_set:
                return dist + 1
            if nxt2 not in visited:
                visited.add(nxt2)
                queue.append((nxt2, dist + 1))
    return float('inf')


# Find a minimal generating set for PSL(2,7)
# PSL(2,7) is generated by an element of order 2 and an element of order 3
# (or order 2 and order 7). Let's find a small generating set.
def find_generators(mul_table, ords, ID):
    """Find a minimal generating set for PSL(2,7)."""
    from collections import deque
    # Try pairs (order 2, order 3) and (order 2, order 7)
    ord2 = [i for i in range(168) if ords[i] == 2]
    ord3 = [i for i in range(168) if ords[i] == 3]
    ord7 = [i for i in range(168) if ords[i] == 7]

    for a in ord2:
        for b in ord3[:5]:  # try a few
            # Check if {a, b} generates the full group
            generated = {ID}
            frontier = {ID, a, b}
            while frontier - generated:
                generated.update(frontier)
                new = set()
                for x in frontier:
                    for g in [a, b]:
                        new.add(mul_table[x, g])
                        new.add(mul_table[g, x])
                frontier = new - generated
            if len(generated) == 168:
                return [a, b]

    # Fallback: try order 2 + order 7
    for a in ord2:
        for b in ord7[:3]:
            generated = {ID}
            frontier = {ID, a, b}
            while frontier - generated:
                generated.update(frontier)
                new = set()
                for x in frontier:
                    for g in [a, b]:
                        new.add(mul_table[x, g])
                        new.add(mul_table[g, x])
                frontier = new - generated
            if len(generated) == 168:
                return [a, b]

    return None

