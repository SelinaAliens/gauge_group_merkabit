# Compute diameter of the Cayley graph (max BFS distance between any two elements)
from collections import deque

def bfs_all_distances(start, gen_set, mul_table):
    """BFS from start, return dict of distances to all reachable elements."""
    dist = {start: 0}
    queue = deque([start])
    while queue:
        current = queue.popleft()
        for g in gen_set:
            for nxt in [mul_table[current, g], mul_table[g, current]]:
                if nxt not in dist:
                    dist[nxt] = dist[current] + 1
                    queue.append(nxt)
    return dist

# Compute from a few representative elements to find diameter
