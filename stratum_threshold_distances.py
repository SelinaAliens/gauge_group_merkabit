# Verify they generate the full group
def verify_generation(gens, mul_table, ID):
    generated = {ID}
    frontier = set(gens) | {ID}
    while frontier - generated:
        generated.update(frontier)
        new = set()
        for x in frontier:
            for g in gens:
                new.add(mul_table[x, g])
                new.add(mul_table[g, x])
        frontier = new - generated
    return len(generated)

